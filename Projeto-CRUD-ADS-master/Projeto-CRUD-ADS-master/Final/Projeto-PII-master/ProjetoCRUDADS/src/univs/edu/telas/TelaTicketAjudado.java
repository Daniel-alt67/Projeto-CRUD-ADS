    private void EnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EnviarActionPerformed
        if(!jMensagem.getText().equals("")){
    ticket.setMensagem(jMensagem.getText());
    ticket.setAssunto(jAssunto.getText());
    ticket.setUsuario(UsuarioAjudado.usuario);
    if(rOutros.isSelected()){
        ticket.setProblema(tfOutros.getText());
    }
    ticketDAO.salvar(ticket);
   }
        dispose();
    }//GEN-LAST:event_EnviarActionPerformed

